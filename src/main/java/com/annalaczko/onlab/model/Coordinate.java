package com.annalaczko.onlab.model;

public class Coordinate {
    private double Y;
    private double X;

    public double getX() {
        return X;
    }

    public double getY() {
        return Y;
    }

    public void setX(double x) {
        X = x;
    }

    public void setY(double y) {
        Y = y;
    }
}
